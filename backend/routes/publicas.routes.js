publicas.get('/actualizarMotivo', async (req, res) => {
	const nuevos = []
	try {
		for (const documento of documentos) {
			///////////////// ---Agregar horario, tratamiento y precio--- /////////////////
			if (documento.motivo.includes('Tratamiento')) {
				documento.motivo = `Masaje ${documento.motivo.split(' ')[1]}`
			}
			let tratamiento = await Tratamiento.findOne({
				nombre: {
					$regex: new RegExp(documento.motivo || documento.tratamiento, 'i'),
				},
			})
			if (tratamiento) {
				const sesion = tratamiento.sesiones.find(
					(s) => s.descripcion === '1 sesion'
				)
				documento.precio = sesion.precio
				documento.tratamiento = {
					tratamientoId: tratamiento._id,
					nombre: tratamiento.nombre,
					sesiones: 1,
					descripcionSesion: sesion.descripcion,
				}
			} else {
				documento.tratamiento = {
					tratamientoId: '6514a25ade2423efc8edf500',
					nombre: 'Masaje Estético',
					sesiones: 1,
					descripcionSesion: '1 sesion',
				}
				documento.precio = 890
			}
			//////////////////////////////////////////////////
			if (documento.hora && documento.fecha) {
				const fecha = new Date(documento.fecha).toISOString().split('T')[0]
				const horaInicio = new Date(`${fecha} ${documento.hora}`)
				horaInicio.setHours(documento.hora.split(':')[0]) // Establecer la hora desde 'hora'
				horaInicio.setMinutes(documento.hora.split(':')[1]) // Establecer los minutos desde 'hora'
				const horaFin = new Date(horaInicio.getTime() + 30 * 60000) // Sumar 30 minutos en milisegundos
				documento.horario = {
					horaInicio: horaInicio.toISOString(),
					horaDeFin: horaFin.toISOString(),
				}
			}
			nuevos.push(documento)
		}

		const resultados = await Reserva.insertMany(nuevos)
		res.json({
			mensaje: 'Actualizados todos los documentos.',
			nuevos: resultados,
		})
	} catch (error) {
		console.error(error)
		res.status(500).send('Error al actualizar.')
	}
})

